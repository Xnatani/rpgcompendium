def calc_mod(value):
    if value == 1:
        return -5
    elif value in range(2, 4):
        return -4
    elif value in range(4, 6):
        return -3
    elif value in range(6, 8):
        return -2
    elif value in range(8, 10):
        return -1
    elif value in range(10, 12):
        return 0
    elif value in range(12, 14):
        return 1
    elif value in range(14, 16):
        return 2
    elif value in range(16, 18):
        return 3
    elif value in range(18, 20):
        return 4
    elif value in range(20, 22):
        return 5
    elif value in range(22, 24):
        return 6
    elif value in range(24, 26):
        return 7
    elif value in range(26, 28):
        return 8
    elif value in range(28, 30):
        return 9
    elif value == 30:
        return 10
